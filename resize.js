const fs = require("fs");
const rimraf = require("rimraf");
const sharp = require("sharp");
const minify = require("html-minifier").minify;
const base = "./img";
const template = "index.template.html";
const sizes = [420, 840];
const skip_images = process.argv.includes("--skip-images");
let processed_count = 0;
let images_html = "";

if (!skip_images) {
  sizes.forEach(size => {
    rimraf(`${base}/${size}/*`, err => {
      if (err) {
        console.log(err);
      }
    });
  });
  rimraf(`${base}/full/*`, err => {
    if (err) {
      console.log(err);
    }
  });
}

fs.readdir(`${base}/original`, (err, files) => {
  if (err) {
    console.log;
    `Couldn't read image files. ${err}`;
  } else {
    const total_files = files.length;
    files.forEach(file => {
      file = file.replace(".jpg", "");
      let sources_html = "";

      sizes.forEach(size => {
        // resize images
        if (!skip_images) {
          setTimeout(() => {
            resize(file, size);
          }, 1000);

          setTimeout(() => {
            fullSize(file);
          }, 2000);
        }

        // add HTML for this size
        sources_html += build_sources_html(size, file);
      });

      // build <source>s into <a> and <picture> tags for this image
      sharp(`img/original/${file}.jpg`)
        .metadata()
        .then(metadata => {
          output_html(file, sources_html, sizes, [
            metadata.width,
            metadata.height
          ]);
        })
        .catch(err => console.log(err));

      // if this is the final file, build index.html
      if (++processed_count === total_files) {
        save_processed_files();
      }
    });
  }
});

const build_sources_html = (size, file) => {
  return `
          <source
            type="image/webp"
            media="(max-width: ${size}px)"
            data-srcset="img/${size}/${file}.webp">                      
          <source
            type="image/jpeg"
            media="(max-width: ${size}px)"
            data-srcset="img/${size}/${file}.jpg">                      
        `;
};

const output_html = (file, sources_html, sizes, dimensions) => {
  const aspect = `${(dimensions[1] / dimensions[0]) * 100}%`;
  const lastSize = sizes[sizes.length - 1];
  // output HTML for files
  images_html += `<a
                    href="img/full/${file}.jpg"
                    target="_blank">
                    <picture data-file="${file.toLowerCase()}" style="padding-top: ${aspect};">
                      ${sources_html}
                      <source
                        type="image/webp"
                        data-srcset="img/full/${file}.webp"> 
                      <img class="lazyload" data-src="img/full/${file}.jpg">
                    </picture>
                  </a>`;
};

const resize = (file, width) => {
  const image = sharp(`${base}/original/${file}.jpg`);

  // webp version
  image
    .resize(width)
    .toFormat("webp")
    .toFile(`./img/${width}/${file}.webp`, (err, info) => {
      if (err) {
        console.log(`Uh oh! Couldn't save output file. ${err}`);
      } else {
        console.log(`Saved img/${width}/${file}.webp`);
      }
    });

  // jpg backup
  image
    .resize(width)
    .toFormat("jpg")
    .toFile(`./img/${width}/${file}.jpg`, (err, info) => {
      if (err) {
        console.log(`Uh oh! Couldn't save output file. ${err}`);
      } else {
        console.log(`Saved img/${width}/${file}.jpg`);
      }
    });
};

const fullSize = file => {
  const image = sharp(`${base}/original/${file}.jpg`);

  // webp version
  image.toFormat("webp").toFile(`./img/full/${file}.webp`, (err, info) => {
    if (err) {
      console.log(`Uh oh! Couldn't save output file. ${err}`);
    } else {
      console.log(`Saved img/full/${file}.webp`);
    }
  });

  // jpg backup
  image.toFormat("jpg").toFile(`./img/full/${file}.jpg`, (err, info) => {
    if (err) {
      console.log(`Uh oh! Couldn't save output file. ${err}`);
    } else {
      console.log(`Saved img/full/${file}.jpg`);
    }
  });
};

const save_processed_files = () => {
  fs.readFile(template, "utf-8", (err, data) => {
    if (err) {
      console.log(`Couldn't read ${template}. ${err}`);
    } else {
      const html = data.replace("{{the_content}}", images_html);
      const minified = minify(html, {
        minifyCSS: true,
        html5: true,
        collapseWhitespace: true,
        collapseInlineTagWhitespace: true,
        decodeEntities: true,
        includeAutoGeneratedTags: true,
        removeComments: true
      });
      fs.writeFile("./index.html", minified, written_files);
    }
  });
};

const written_files = () => {
  console.log(`Saved index.html`);
};
